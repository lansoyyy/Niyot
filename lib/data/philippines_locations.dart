  static List<String> get provinces {
    final keys = citiesByProvince.keys.toList();
    keys.sort((a, b) {
      if (a == 'Other / Not listed') return 1;
      if (b == 'Other / Not listed') return -1;
      return a.compareTo(b);
    });
    return keys;
  }

  static List<String> citiesForProvince(String province) {
    if (province.isEmpty) return const [];
    return List<String>.from(citiesByProvince[province] ?? const []);
  }

  static List<String> get allCityNames {
    final set = <String>{};
    for (final cities in citiesByProvince.values) {
      set.addAll(cities);
    }
    return set.toList()..sort();
  }

  static List<String> venueSuggestionsForQuery(String query, {int limit = 8}) {
    final q = query.trim().toLowerCase();
    if (q.length < 2) return const [];
    final matches = <String>[];
    for (final city in allCityNames) {
      if (city.toLowerCase().contains(q)) {
        matches.add(city);
        if (matches.length >= limit) break;
      }
    }
    return matches;
  }

  static String composeLocation({
    required String city,
    required String province,
    String country = countryName,
  }) {
    final parts = [city, province, country]
        .map((e) => e.trim())
        .where((e) => e.isNotEmpty)
        .toList();
    return parts.join(', ');
  }
